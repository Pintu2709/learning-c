#include <stdio.h>
// creat variables
int main()
{
    // Integer (Whole number)
    int myNum = 10;
    // Floating point number
    float myFloatNum = 10.999;
    char myLetter = 'D';
    // print variables
    printf("%d\n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
    return 0;
}